    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
        try {
        String patente = jTextField5.getText().trim();
        String nombreMarca = jTextField4.getText().trim();
        String paisMarca = jTextField3.getText().trim();
        String modelo = jTextField2.getText().trim();
        int anio = Integer.parseInt(jTextField1.getText().trim());
        double capacidadCarga = Double.parseDouble(jTextField6.getText().trim());

        String codigoSucursal = (String) jComboBox2.getSelectedItem();
        java.util.Optional<domain.Sucursal> sucursalOpt = data.Persistencia.getSucursal(codigoSucursal);

        if (!sucursalOpt.isPresent()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar una sucursal válida.");
            return;
        }

        domain.Sucursal sucursal = sucursalOpt.get();
        String tipo = cmbTipoVehiculo.getSelectedItem().toString();

        if (patente.isEmpty() || nombreMarca.isEmpty() || paisMarca.isEmpty() || modelo.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Completa todos los campos obligatorios.");
            return;
        }

        domain.Marca marca = new domain.Marca(nombreMarca, paisMarca);
        domain.Vehiculo vehiculo;

        if (tipo.equalsIgnoreCase("Electrico")) {
            double kwhBase = Double.parseDouble(txtKwhBase.getText().trim());

            vehiculo = new domain.VehiculoElectrico(
                    patente, marca, modelo, anio, capacidadCarga, sucursal, kwhBase
            );
        } else {
            double kmPorLitro = Double.parseDouble(txtKmPorLitro.getText().trim());
            double litrosExtra = Double.parseDouble(txtLitrosExtra.getText().trim());

            vehiculo = new domain.VehiculoCombustible(
                    patente, marca, modelo, anio, capacidadCarga, sucursal, kmPorLitro, litrosExtra
            );
        }

        data.Persistencia.agregarVehiculo(vehiculo);

        javax.swing.JOptionPane.showMessageDialog(this, "Vehículo agregado correctamente.");
        limpiarCampos();

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Revisa los campos numéricos.");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al agregar vehículo: " + e.getMessage());
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

