    private void actualizarPanelesTipo() {
    String tipo = cmbTipoVehiculo.getSelectedItem().toString();

    if (tipo.equalsIgnoreCase("ELECTRICO") || tipo.equalsIgnoreCase("Electrico")) {
        panelElectrico.setVisible(true);
        panelCombustible.setVisible(false);
    } else {
        panelElectrico.setVisible(false);
        panelCombustible.setVisible(true);      
    }

    revalidate();
    repaint();
}



